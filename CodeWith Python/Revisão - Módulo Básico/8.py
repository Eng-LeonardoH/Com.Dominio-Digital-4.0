# 8. Faça um código que receba 4
# números e realize a soma deles e a
# média entre eles. e mostre os
# resultados.

num0 = float(input("Digite o 1ª número: "))
num1 = float(input("Digite o 2ª número: "))
num2 = float(input("Digite o 3ª número: "))
num3 = float(input("Digite o 4ª número: "))
soma = num0 + num1 + num2 + num3
media = soma / 4
print(f"A noma dos números é {soma} e a média é {media}.")
