# Programa para receber um número e retornar se ele é par ou impar

# Início do Programa

# Declaração de variáveis
numero = int(input("Insira um número inteiro: "))

# Processamento
if (numero % 2) == 1:
    print("O número informado é impar.")
else:
    print("O número informado é par.")

