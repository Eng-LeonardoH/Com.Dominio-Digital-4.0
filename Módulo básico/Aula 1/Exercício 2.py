# Receber dois números e mostrar na tela, depois exibir sua soma
numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))
print("Os números digitados foram:", '\n', numero1, '\n', numero2, '\n', '\n',
      end="Esses foram os números digitados. \n")
soma_numeros_1_e_2 = (numero1) * (numero2)

print("A soma dos números é", soma_numeros_1_e_2, end=".\n")
